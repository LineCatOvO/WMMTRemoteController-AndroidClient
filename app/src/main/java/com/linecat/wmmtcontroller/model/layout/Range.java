package com.linecat.wmmtcontroller.model.layout;

/**
 * 范围类
 */
public class Range {
    private Double min;
    private Double max;

    public Range() {}

    public Double getMin() {
        return min;
    }

    public void setMin(Double min) {
        this.min = min;
    }

    public Double getMax() {
        return max;
    }

    public void setMax(Double max) {
        this.max = max;
    }
}